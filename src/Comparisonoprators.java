
public class Comparisonoprators {

	public static void main(String[] args) {
		//  == oprator
		int a=10,b=10;//if b=15 false
		System.out.println(a==b);
		
		//not equal to  !=
		int a1=10,b1=20;// if b1=10 false
		System.out.println(a1!=b1);
		
		//  > oparator
		
		int a3=10,b3=5;
		System.out.println(a3>b3);
		
		// < oprator
		int a4=10,b4=20;
		System.out.println(a4<b4);
		// greater than  >= oprator
		int a5=20,b5=50;
		System.out.println(a5>=b5);
		// less than equal to <=
		int a6=10,b6=20;
		System.out.println(a6<=b6);
		
		

	}

}
