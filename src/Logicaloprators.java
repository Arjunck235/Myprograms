
public class Logicaloprators {

	public static void main(String[] args) {
		//&& >> true:: both statements are true
		int a=10;
		System.out.println(a>5 && a<20);
		
		//  || :: true > if one statement is true
		int a1=5;
		System.out.println(a1>3 || a<10);
		// logical not ! it gives opposite result
		int a2=15;
		System.out.println(!(a2>10));
		
		
	}

}
